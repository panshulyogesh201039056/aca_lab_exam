	AREA NIB, CODE, READONLY
	ENTRY
START
	LDR R0,VALUE    ;LOADIND THE ADDRESS INTO R0 REG
	ldr R1,[R0]     ;LOADING VALUE IN R0 TO R1
	LDR R2,[R0]		;LOADING VALUE IN R0 TOM R2
	AND R1,R1,0X000F0000   ;ANDIND BITS OF R1
	AND R2,R2,0X0000000F	;ANDING BITS OF R2
	MOV R1,R1,LSR #16 ; LOGICAL SHIFT RIGHT BY 16 BITS
	ADD R4,R1,R2	;R4<-R1+R2
	LDR R5,RESULT ; LOADING ADDRESS OF RESULT INTO R5
	STR R4,[R5]   ;STORING THE FINAL RESULT IN R4

STOP

VALUE DCD 0X40000004  ;STARTING ADDRESS
RESULT DCD 0X4000000c ;FINAL ADDRESS
	END
